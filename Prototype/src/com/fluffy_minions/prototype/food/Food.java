package com.fluffy_minions.prototype.food;

/**
 * This class provides the fields of every table that contains a type of food (GuiltyPleasures, Dulciuri etc.).
 */

public abstract class Food {

    /**
     * These are the fields that every table contains.
     */

    public static final String id = "_id";
    public static final String name = "NAME";
	public static final String breakfast = "BREAKFAST";
	public static final String lunch = "LUNCH";
	public static final String dinner = "DINNER";
	public static final String needsSidedish = "NEEDS_SIDEDISH";
	public static final String isSidedish = "IS_SIDEDISH";
	public static final String calories = "CALORIES";
	public static final String proteins = "PROTEINS";
	public static final String fats = "FATS";
	public static final String carbs = "CARBS";
	public static final String fiber = "FIBER";
	public static final String vitamins = "VITAMINS";
	public static final String minerals = "MINERALS";

}
